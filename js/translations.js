// Language switcher functionality
class LanguageSwitcher {
    constructor() {
        this.currentLang = localStorage.getItem('language') || 'en';
        this.init();
    }

    init() {
        // Wait for DOM to be ready
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', () => {
                this.setupLanguageSwitcher();
            });
        } else {
            this.setupLanguageSwitcher();
        }
    }

    setupLanguageSwitcher() {
        this.addLanguageSwitcher();
        this.applyLanguage(this.currentLang);
        this.bindEvents();
    }

    addLanguageSwitcher() {
        // Try multiple possible locations for the language switcher
        const possibleLocations = [
            '.top-bar-right .social',
            '.top-bar-right',
            '.navbar-nav',
            '.navbar .container-fluid'
        ];

        let targetElement = null;
        for (const selector of possibleLocations) {
            targetElement = document.querySelector(selector);
            if (targetElement) break;
        }

        if (targetElement) {
            // Check if language switcher already exists
            if (document.querySelector('.language-switcher')) {
                return;
            }

            const langSwitcher = document.createElement('div');
            langSwitcher.className = 'language-switcher';
            langSwitcher.innerHTML = `
                <div class="lang-dropdown">
                    <button class="lang-btn" type="button">
                        <span class="current-lang">${this.currentLang.toUpperCase()}</span>
                        <i class="fa fa-chevron-down"></i>
                    </button>
                    <div class="lang-dropdown-menu">
                        <a href="#" class="lang-option" data-lang="en">
                            <span class="flag">🇺🇸</span>
                            <span class="lang-name">${translations[this.currentLang]['lang.en']}</span>
                        </a>
                        <a href="#" class="lang-option" data-lang="fr">
                            <span class="flag">🇫🇷</span>
                            <span class="lang-name">${translations[this.currentLang]['lang.fr']}</span>
                        </a>
                    </div>
                </div>
            `;

            // Insert before the target element
            targetElement.parentNode.insertBefore(langSwitcher, targetElement);
        }
    }

    bindEvents() {
        // Language switcher events
        document.addEventListener('click', (e) => {
            if (e.target.closest('.lang-btn')) {
                e.preventDefault();
                const dropdown = e.target.closest('.lang-dropdown');
                dropdown.classList.toggle('active');
            } else if (e.target.closest('.lang-option')) {
                e.preventDefault();
                const lang = e.target.closest('.lang-option').dataset.lang;
                this.switchLanguage(lang);
            } else {
                // Close dropdown when clicking outside
                document.querySelectorAll('.lang-dropdown.active').forEach(dropdown => {
                    dropdown.classList.remove('active');
                });
            }
        });

        // Close dropdown on escape key
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') {
                document.querySelectorAll('.lang-dropdown.active').forEach(dropdown => {
                    dropdown.classList.remove('active');
                });
            }
        });
    }

    switchLanguage(lang) {
        if (lang !== this.currentLang && translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('language', lang);
            this.applyLanguage(lang);
            this.updateLanguageSwitcher();
            
            // Close dropdown
            document.querySelectorAll('.lang-dropdown.active').forEach(dropdown => {
                dropdown.classList.remove('active');
            });
        }
    }

    applyLanguage(lang) {
        if (!translations[lang]) {
            console.warn(`Language ${lang} not found in translations`);
            return;
        }

        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang] && translations[lang][key]) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translations[lang][key];
                } else {
                    element.textContent = translations[lang][key];
                }
            }
        });

        // Update HTML lang attribute
        document.documentElement.lang = lang;
        
        // Update page title if it has a translation
        const titleKey = 'page.title';
        if (translations[lang][titleKey]) {
            document.title = translations[lang][titleKey];
        }
    }

    updateLanguageSwitcher() {
        const currentLangSpan = document.querySelector('.current-lang');
        if (currentLangSpan) {
            currentLangSpan.textContent = this.currentLang.toUpperCase();
        }

        // Update language names in dropdown
        const langOptions = document.querySelectorAll('.lang-option .lang-name');
        langOptions.forEach(option => {
            const lang = option.closest('.lang-option').dataset.lang;
            if (translations[this.currentLang] && translations[this.currentLang][`lang.${lang}`]) {
                option.textContent = translations[this.currentLang][`lang.${lang}`];
            }
        });
    }
}

// Initialize language switcher
